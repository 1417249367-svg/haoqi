///////////////////////////////////////////////////////////////////////////////////////////
//upload init 上传初始化
///////////////////////////////////////////////////////////////////////////////////////////
function upload_init()
{
	/*upload list switch button*/
	$(".btn_switch").click(function(){
		$('.upload_content').toggle();
		if ($('.upload_content').is(':hidden'))
			$(this).attr("class","icon icon_win_minus");
		else
			$(this).attr("class","icon icon_win_plus");

	})
	
	$(".btn_close").click(function(){
		upload_list_hide();
	})
}


