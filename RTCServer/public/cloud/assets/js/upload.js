///////////////////////////////////////////////////////////////////////////////////////////
//upload_list_add 添加到上传列表
///////////////////////////////////////////////////////////////////////////////////////////
function upload_list_add(file_type,file_name,file_size)
{
	var html = "";
	html += "<tr>" ;
		html += "<td><span class='icon icon_down'></span>" + file_name + "</td>";
		html += "<td>" + get_filesize(file_size) + "</td>";
		html += "<td>/</td>";
		html += "<td><span class='icon icon_success'></span></td>";
	html += "</tr>";
	$("#upload_container tbody").append(html);
}

